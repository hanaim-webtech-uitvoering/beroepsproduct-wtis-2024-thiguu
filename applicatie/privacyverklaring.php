<h2>Privacyverklaring Pizzeria Sole Machina</h2>

<p>
Bij Pizzeria Sole Machina vinden we jouw privacy belangrijk. In deze verklaring leggen we uit welke gegevens we verzamelen en wat we ermee doen.
</p>

<h3>Welke gegevens verzamelen we?</h3>
<p>Wanneer je je registreert en bestellingen plaatst, verzamelen we de volgende gegevens:</p>
<ul>
    <li>Voor- en achternaam</li>
    <li>Gebruikersnaam</li>
    <li>Wachtwoord (veilig gehashed)</li>
    <li>Afleveradres bij bestelling</li>
    <li>Bestelgeschiedenis</li>
</ul>

<h3>Waarom verzamelen we deze gegevens?</h3>
<p>We verzamelen jouw gegevens om de volgende redenen:</p>
<ul>
    <li>Om bestellingen correct te kunnen verwerken en bezorgen</li>
    <li>Om je in te laten loggen op je account</li>
    <li>Om je inzage te geven in je bestelgeschiedenis</li>
</ul>

<h3>Wie heeft toegang tot je gegevens?</h3>
<p>Alleen geautoriseerd personeel van Sole Machina heeft toegang tot de gegevens. Deze worden niet gedeeld met derden.</p>

<h3>Hoelang bewaren we je gegevens?</h3>
<p>Je gegevens worden bewaard zolang je account actief is. Als je je account laat verwijderen, worden je gegevens verwijderd.</p>

